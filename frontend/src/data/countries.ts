// Helper to get country by ISO code
export function getCountryByCode(isoCode: string): CountryData | undefined {
  return countriesData[isoCode.toUpperCase()];
}

// Get all available countries
export function getAllCountries(): CountryData[] {
  return Object.values(countriesData);
}

// Get country codes list
export function getCountryCodes(): string[] {
  return Object.keys(countriesData);
}

// Search countries by name
export function searchCountries(query: string): CountryData[] {
  const lowerQuery = query.toLowerCase();
  return Object.values(countriesData).filter(
    (country) =>
      country.identity.name.toLowerCase().includes(lowerQuery) ||
      country.identity.capital.toLowerCase().includes(lowerQuery)
  );
}
